import turtle

def setup_turtle():
    turtle.Screen().clear()  # Clears the screen
    turtle.speed(0)  # Sets the Turtle's speed to the fastest (0)
    turtle.pensize(1)  # Sets the pen size to 1 pixel
    turtle.bgcolor("white")  # Sets the background color to white
    turtle.title("Courbe du dragon")  # Sets the window title to "Courbe du dragon"